namespace ExcelTemplate.Style
{
    public enum ETBorderStyle
    {
        /// <summary>
        /// 无边框
        /// </summary>
        None = 0,

        /// <summary>
        /// 细实线（默认细边框）
        /// </summary>
        Thin = 1,

        /// <summary>
        /// 中粗实线
        /// </summary>
        Medium = 2,

        /// <summary>
        /// 虚线边框
        /// </summary>
        Dashed = 3,

        /// <summary>
        /// 点线边框
        /// </summary>
        Dotted = 4,

        /// <summary>
        /// 粗实线
        /// </summary>
        Thick = 5,

        /// <summary>
        /// 双线边框
        /// </summary>
        Double = 6,

        /// <summary>
        /// 细线（比Thin更细）
        /// </summary>
        Hair = 7,

        /// <summary>
        /// 中等粗细虚线
        /// </summary>
        MediumDashed = 8,

        /// <summary>
        /// 点划线（一点一划）
        /// </summary>
        DashDot = 9,

        /// <summary>
        /// 中等粗细点划线
        /// </summary>
        MediumDashDot = 10,

        /// <summary>
        /// 双点划线（一点两划）
        /// </summary>
        DashDotDot = 11,

        /// <summary>
        /// 中等粗细双点划线
        /// </summary>
        MediumDashDotDot = 12,

        /// <summary>
        /// 斜点划线（倾斜样式）
        /// </summary>
        SlantedDashDot = 13
    }
}
